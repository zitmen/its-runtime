package mi.run.runtime.built_in_routines;

public class MathRoutines
{
    public static double pow(double x, double n)
    {
        return Math.pow(x, n);
    }
    
    public static double sqrt(double x)
    {
        return Math.sqrt(x);
    }
    
    public static double log(double x)
    {
        return Math.log(x);
    }
}
